package Calculator;

public class Calc {
public double a;
public double b;

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }

    public Calc(double a, double b) {
        this.a = a;
        this.b = b;
    }
    public double add(){
        return a+b;
    }
    public double action (){
        return action();
    }
}
